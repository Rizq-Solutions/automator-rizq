exports.createNew = function(req, res) {
helper.checkPermission(req,"a",function (isPermited) {
        if(isPermited){
  var reqObj = new Balance(req,req.body);
   var createObj = {
    
id:0,
user_id:req.body.user_id,
total_balance:req.body.total_balance,
used_balance:req.body.used_balance,
current_balance:req.body.current_balance,
last_balance:req.body.last_balance,
pending_balance:req.body.pending_balance,
last_pending_balance:req.body.last_pending_balance,
exceed_balance:req.body.exceed_balance,
balance_after_topup:req.body.balance_after_topup,
last_topup_balance:req.body.last_topup_balance,
last_topup_at: new Date(),
version:req.body.version,
updated_at:req.body.updated_at,
created_at:req.body.created_at,
    };
   if(!createObj.user_id || !createObj.total_balance || !createObj.used_balance || !createObj.current_balance || !createObj.last_balance || !createObj.exceed_balance || !createObj.version){

            res.status(400).send({ error:true, message: 'Please provide required fields' });

        }
else{
 
  Balance.create(req,createObj, function(err, balance) {
    
	  if (err){
      res.status(200).send(helper.createResponse(helper.Error,0,err,""));
	  }else{
    res.status(200).send(helper.createResponse(helper.Success,1,"Record Created",balance));
	}
  });
}
} else{
            res.status(403).send(helper.createResponse(helper.Error,0,helper.authError,""));
		}
    });
};


